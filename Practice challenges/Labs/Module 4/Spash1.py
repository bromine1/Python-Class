def greet(firstName):
    return ("Hey,", firstName,"!".format(name = firstName))

print(greet("You"))
print(greet("Ryan"))
print(greet("Bruce"))

def greeting(firstName, lastName):
    return ("Hey, {} {lname}!".format(firstName, lname = lastName))

print(greeting("You", "Friend"))
print(greeting("Ryan", "Stauffer"))
print(greeting("Bruce", "Wayne"))





